<h1>Database Integration Demo (1)</h1>
<p>The example demonstrates creating a chart using data from a database.</p>
<p>This example is based on the MySQL database. To run this example, you need to:</p>
<ul>
	<li>Verify that your PHP has MySQL extensions installed<br>&nbsp;</li>
	<li>Set up a MySQL server to run on the same machine as the web server<br>&nbsp;</li>
	<li>Create a database called "sample" in your MySQL server<br>&nbsp;</li>
	<li>Create an account with username "test" and password "test" that can be used to access "sample"<br>&nbsp;</li>
	<li>Import the table "sample.sql" (located in the "phpdemo" subdirectory) into the "sample" database</li>
</ul>

<p>After you have performed the above, please <a href="dbdemo_test.php">click here</a> to check the 
database connection.</p>

<p>If the database connection works fine, you may <a href="dbdemo1.php">click here</a> to run this demo.</p>

